double
fadd64(double arg1, double arg2)
{
    double src = arg1;
    double dest = arg2;

    dest += src;

    return dest;
}

double
fsub64(double arg1, double arg2)
{
    double src = arg1;
    double dest = arg2;

    dest -= src;

    return dest;
}

double
fmul64(double arg1, double arg2)
{
    double src = arg1;
    double dest = arg2;

    dest *= src;

    return dest;
}

double
fdiv64(double arg1, double arg2)
{
    double src = arg1;
    double dest = arg2;

    dest /= src;

    return dest;
}

